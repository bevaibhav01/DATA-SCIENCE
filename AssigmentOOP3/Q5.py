
# Q5. Can we create an instance of an abstract class? Explain your answer.

# No, we cannot create an instance of an abstract class directly in Python.
# An abstract class is a class that contains one or more abstract methods
# (methods without any implementation) and is meant to be a blueprint for other
# classes to inherit from. It serves as a template for its subclasses and
# provides a common interface that its subclasses must follow.